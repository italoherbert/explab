package italo.explab.util;

public class TipoRetornoInvalidoGFuncException extends Exception {

    public TipoRetornoInvalidoGFuncException() {
    }

    public TipoRetornoInvalidoGFuncException(String message) {
        super(message);
    }

    public TipoRetornoInvalidoGFuncException(String message, Throwable cause) {
        super(message, cause);
    }

    public TipoRetornoInvalidoGFuncException(Throwable cause) {
        super(cause);
    }

    public TipoRetornoInvalidoGFuncException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
    
}
