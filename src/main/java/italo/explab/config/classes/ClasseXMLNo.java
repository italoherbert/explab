package italo.explab.config.classes;

public class ClasseXMLNo {
    
    private final String dir;
    private final String arq;

    public ClasseXMLNo(String dir, String arq) {
        this.dir = dir;
        this.arq = arq;
    }

    public String getDir() {
        return dir;
    }

    public String getArq() {
        return arq;
    }
    
}
